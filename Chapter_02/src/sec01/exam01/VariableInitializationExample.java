package sec01.exam01;

public class VariableInitializationExample {

	public static void main(String[] args) {
		//변수 Value 선언
		//int value;
		int value = 100;
		
		//변수 value값을 읽고 10을 더하느 산술 연산을 진행
		//연산의 결과 값을 변수 result의 초기값으로 저장
		int result = value + 10;
		
		//변수 result 값을 읽고 콘솔에 출력
		System.out.println(result);
		
		
		//변수 value가 선언되었지만, 초기화되지 않았기 때문에 2라인의 value 변수는 존재하지 않습니다.

	}

}
